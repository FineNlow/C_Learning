/*
*Constants*
If you want to change a variables values, use `const` to make it
__EX__
*/

#include <stdio.h>

int main() {
    const int NUMBER = 10;
    const float PI = 3.14;
    printf("%d\n", NUMBER);
    printf("%.2f", PI);
}

